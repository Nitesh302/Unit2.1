public class Calculate {
   public int Add(int a, int b){
       int res = a+b;
       return res;
   }
   public int Sub(int a, int b){
       int res = a-b;
       return res;
   }
   public int multi(int a, int b){
       int res = a*b;
       return res;
   }
   public int Div(int a,int b){
       int res = a/b;
       return res;
   }
}
